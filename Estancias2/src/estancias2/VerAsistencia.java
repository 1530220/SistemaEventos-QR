    //funcion para realizar la busqueda de asistencias marcadas para darle un reporte de asistencias al administrador, con el respectivo usuario y evento
    void Busqueda(){
        try {
            DefaultTableModel model= new DefaultTableModel();
            
            model.addColumn("Clave de Evento");
            model.addColumn("Nombre de Evento");
            model.addColumn("Matricula de usuario que asisti√≥");
            model.addColumn("Hora de registro de asistencia");
            

            busqueda_table.setModel(model);
            
            String consultar="SELECT clave_evento,nombre_evento,matricula_usuario,hora FROM asistencia";

            String []datos= new String[4];
            
            Statement st=cn.createStatement();
            ResultSet rs=st.executeQuery(consultar);
            
            while(rs.next()){
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                
                model.addRow(datos);
            }
          //  tbpro.setModel(model);
        } catch (SQLException ex) {
            showMessageDialog(null, "No se pudo realizar busqueda de asistencias.");
        }
    }
    
